my_string = "Curso de Codigo Facilito"

print(my_string)
print(my_string[0])#Esto me printa la primera posición del string "my_string"
print(my_string[-1])#Esto me printa la última posición del string "my_string"
print(my_string[0], my_string[9]) #Mostrará amabas posiciones
print(my_string[0:10])#Esto me printa de la posición 9 de my_string #La última posición indicada no la mostrará (10)
print(my_string[0:10:2])#Me mostrará de la 0 a la 10 de 2 en 2
